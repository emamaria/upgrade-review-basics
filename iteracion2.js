function averageVolume(){

let count = 0;

let sum = 0

for(let user of users){
    for(let type in user.favoritesSounds){
         sum += user.favoritesSounds[type].volume;
         count++;
    }
  
}


 
 return "The average volume  of users favourite sounds is " + sum/count;

}


